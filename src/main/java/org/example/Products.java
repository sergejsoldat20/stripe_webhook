package org.example;

public class Products {
    private int page;
    private int perPage;

    // Getters and setters for the fields
    /*
    *
    * @pre()
    * @post()
    *
    * */
    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getPer_page() {
        return perPage;
    }

    public void setPer_page(int per_page) {
        this.perPage = per_page;
    }
}